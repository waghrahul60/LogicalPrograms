//WAP to Display Fibonacci series up to a given
#include <iostream>

using namespace std;

int main()
{

    cout << "Uppercase Alphabets : "<<endl;

    for (char i = 65; i <= 90; i++)
    {
        cout << i << " ";
    }

    cout<<endl;
    cout << "Lowercase Alphabets : "<<endl;

    for (char i = 97; i <= 122; i++)
    {
        cout << i << " ";
    }

    cout<<endl<<"=========================="<<endl;
    cout << "Uppercase Alphabets : "<<endl;

    for (char i = 'A'; i <= 'Z'; i++)
    {
        cout << i << " ";
    }

    cout<<endl;
    cout << "Lowercase Alphabets : "<<endl;

    for (char i = 'a'; i <= 'z'; i++)
    {
        cout << i << " ";
    }

    return 0;
}